"""
Блок 2.3.3: Демо — области видимости (LEGB), global, nonlocal, замыкания
════════════════════════════════════════════════════════════════════════
Темы:
  1. Правило LEGB: Local -> Enclosing -> Global -> Built-in
  2. Чтение глобальной переменной из функции (не требует ключевых слов)
  3. Ключевое слово global — изменение глобальной переменной
  4. Ловушка UnboundLocalError
  5. Enclosing scope и замыкания (closures)
  6. Ключевое слово nonlocal — изменение переменной внешней функции
  7. Практический паттерн: фабрика функций (function factory)
"""

# ════════════════════════════════════════════════════════════════════════
# 1. Правило LEGB: порядок поиска имени
# ════════════════════════════════════════════════════════════════════════
# При обращении к имени Python ищет его в таком порядке:
#   L - Local     (внутри текущей функции)
#   E - Enclosing (во внешней функции, если функция вложенная)
#   G - Global    (на уровне модуля)
#   B - Built-in  (встроенные имена: len, print, str...)
# Поиск останавливается на первом найденном совпадении.

city = "Moscow"  # глобальная переменная


def show_city() -> None:
    """Читает глобальную переменную — global не нужен для ЧТЕНИЯ."""
    print(city)  # Python не находит city локально -> идёт в Global


show_city()  # Moscow


# ════════════════════════════════════════════════════════════════════════
# 2. Локальная переменная затеняет (shadow) глобальную
# ════════════════════════════════════════════════════════════════════════

def local_city() -> None:
    city = "London"  # это НОВАЯ локальная переменная, а не изменение global
    print(city)


local_city()   # London
print(city)    # Moscow — глобальная переменная не изменилась


# ════════════════════════════════════════════════════════════════════════
# 3. global — явное изменение глобальной переменной
# ════════════════════════════════════════════════════════════════════════

counter = 0


def increment() -> None:
    global counter          # без этой строки будет UnboundLocalError
    counter += 1


increment()
increment()
increment()
print(counter)  # 3


# ════════════════════════════════════════════════════════════════════════
# 4. Ловушка UnboundLocalError
# ════════════════════════════════════════════════════════════════════════
# Если внутри функции ЕСТЬ присваивание переменной (даже ниже по коду),
# Python считает её ЛОКАЛЬНОЙ на всю функцию — и чтение ДО присваивания
# вызовет UnboundLocalError, а не возьмёт глобальное значение.

total = 100


def broken() -> None:
    print(total)   # UnboundLocalError: total ещё не связана со значением
    total = 1       # именно эта строка делает total локальной для ВСЕЙ функции


try:
    broken()
except UnboundLocalError as error:
    print(f"Ошибка: {error}")

# Исправление — объявить global ДО использования:
def fixed() -> None:
    global total
    print(total)   # теперь работает: 100
    total = 1


fixed()


# ════════════════════════════════════════════════════════════════════════
# 5. Enclosing scope и замыкания (closures)
# ════════════════════════════════════════════════════════════════════════
# Вложенная функция "помнит" переменные внешней функции даже после
# того, как внешняя функция завершилась. Это и есть замыкание.

def make_greeter(greeting: str):
    def greet(name: str) -> str:
        return f"{greeting}, {name}!"  # greeting берётся из Enclosing scope
    return greet


hello = make_greeter("Привет")
hi = make_greeter("Hi")

print(hello("Аня"))  # Привет, Аня!
print(hi("Anna"))    # Hi, Anna!
# hello и hi — независимые замыкания, каждое со своим greeting


# ════════════════════════════════════════════════════════════════════════
# 6. nonlocal — изменение переменной внешней (enclosing) функции
# ════════════════════════════════════════════════════════════════════════
# Без nonlocal присваивание внутри вложенной функции создаст НОВУЮ
# локальную переменную — точно так же, как без global на уровне модуля.

def make_counter():
    count = 0

    def increment() -> int:
        nonlocal count   # без этой строки — UnboundLocalError при count += 1
        count += 1
        return count

    return increment


counter_a = make_counter()
counter_b = make_counter()

print(counter_a())  # 1
print(counter_a())  # 2
print(counter_a())  # 3
print(counter_b())  # 1 — свой собственный count, не связан с counter_a


# ════════════════════════════════════════════════════════════════════════
# 7. Практический паттерн: накопитель (accumulator) через замыкание
# ════════════════════════════════════════════════════════════════════════

def make_accumulator():
    total = 0

    def add(value: int) -> int:
        nonlocal total
        total += value
        return total

    return add


acc = make_accumulator()
print(acc(10))  # 10
print(acc(5))   # 15
print(acc(20))  # 35
# Замыкания часто используются вместо классов для простого хранения
# состояния между вызовами — это основа для декораторов (тема 5).
