# ============================================================
# ЗАДАЧА 1: print() с параметрами
# ============================================================
# Выведи числа от 1 до 5 в одну строку через дефис:
# 1-2-3-4-5
# Используй один вызов print() с sep=

# ТВОЙ КОД:
print("1", "2", "3", "4", "5", sep="-")


# ============================================================
# ЗАДАЧА 2: print() с end=
# ============================================================
# Используя три отдельных вызова print(), выведи:
# Загрузка...
# (всё в одну строку, каждый print добавляет часть текста)
# print("Загрузка") → print(".") → print("..") → итого: Загрузка...

# ТВОЙ КОД:
print("Загрузка", end="")
print(".", end="")
print("..")


# ============================================================
# ЗАДАЧА 3: input() и типы
# ============================================================
# Симулируй ввод пользователя: число "25"
# Вычисли и выведи: через 10 лет пользователю будет N лет
# Формат: "Через 10 лет вам будет 35 лет"
# Помни: simulated_input — это str, нужно привести тип

simulated_input: str = "25"
# ТВОЙ КОД:
print(f"Через 10 лет вам будет {int(simulated_input) + 10} лет")


# ============================================================
# ЗАДАЧА 4: Запись файла
# ============================================================
# Создай файл "students.txt" и запиши в него 3 строки:
# Анна
# Борис
# Виктор
# Каждое имя на отдельной строке

# ТВОЙ КОД:
with open("students.txt", "w", encoding="utf-8") as f:
    f.write("Анна\n")
    f.write("Борис\n")
    f.write("Виктор\n")

print("Файл students.txt создан ✓")


# ============================================================
# ЗАДАЧА 5: Чтение файла
# ============================================================
# Прочитай файл "students.txt" целиком и выведи его содержимое
# Используй f.read()

# ТВОЙ КОД:
with open("students.txt", "r", encoding="utf-8") as f:
    content = f.read()
    print(content)


# ============================================================
# ЗАДАЧА 6: Перебор строк
# ============================================================
# Прочитай файл "students.txt" построчно
# Выведи каждое имя с номером: "1. Анна", "2. Борис", "3. Виктор"
# Используй enumerate() и strip()

# ТВОЙ КОД:
with open("students.txt", "r", encoding="utf-8") as f:
    for i, line in enumerate(f, start=1):
        print(f"{i}. {line.strip()}")


# ============================================================
# ЗАДАЧА 7: Дозапись
# ============================================================
# Добавь в файл "students.txt" ещё одно имя: "Галина"
# Затем прочитай файл и убедись, что все 4 имени на месте

# ТВОЙ КОД:
with open("students.txt", "a", encoding="utf-8") as f:
    f.write("Галина\n")

# Проверяем, что все 4 имени на месте
with open("students.txt", "r", encoding="utf-8") as f:
    content = f.read()
    print(content)


# ============================================================
# ЗАДАЧА 8: writelines()
# ============================================================
# Есть список оценок:
grades: list[str] = ["Математика: 5\n", "Физика: 4\n", "История: 3\n"]
# Запиши их в файл "grades.txt" через writelines()
# Прочитай файл обратно и выведи каждую строку без лишних пробелов

# ТВОЙ КОД:
with open("grades.txt", "w", encoding="utf-8") as f:
    f.writelines(grades)

# Читаем обратно и выводим без лишних пробелов
with open("grades.txt", "r", encoding="utf-8") as f:
    for line in f:
        print(line.strip())


# ============================================================
# ЗАДАЧА 9: Подсчёт строк
# ============================================================
# Прочитай файл "students.txt" и выведи:
# "В файле N студентов"
# Не используй readlines() — используй for line in f и счётчик

# ТВОЙ КОД:
count = 0
with open("students.txt", "r", encoding="utf-8") as f:
    for line in f:
        count += 1

print(f"В файле {count} студентов")


# ============================================================
# ЗАДАЧА 10: Комплексная задача
# ============================================================
# 1. Создай файл "log.txt"
# 2. Запиши в него 3 строки в формате: "Запись 1: ОК", "Запись 2: ОК", "Запись 3: ОК"
# 3. Дозапись: добавь строку "Запись 4: ОШИБКА"
# 4. Прочитай файл и выведи только строки, содержащие "ОШИБКА"
# Формат вывода: "Найдено: Запись 4: ОШИБКА"

# ТВОЙ КОД:
# 1-2. Создаём файл и записываем первые 3 строки
with open("log.txt", "w", encoding="utf-8") as f:
    f.write("Запись 1: ОК\n")
    f.write("Запись 2: ОК\n")
    f.write("Запись 3: ОК\n")

# 3. Дозаписываем строку с ошибкой
with open("log.txt", "a", encoding="utf-8") as f:
    f.write("Запись 4: ОШИБКА\n")

# 4. Читаем и выводим только строки с "ОШИБКА"
with open("log.txt", "r", encoding="utf-8") as f:
    for line in f:
        if "ОШИБКА" in line:
            print(f"Найдено: {line.strip()}")
