# -*- coding: utf-8 -*-
"""
Блок 2.1 — Тема 2: Переменные, ссылки и объекты
═══════════════════════════════════════════════════

Ключевые концепции:
• Переменная — это ИМЕНО для объекта, не контейнер с данными
• Каждый объект имеет адрес в памяти (id)
• Присваивание x = y создаёт ВТОРОЕ имя, не копирует
• is сравнивает идентичность (один объект?), == сравнивает значения
• Интернирование — оптимизация памяти для маленьких объектов

Запуск: python 03_variables_references_objects_demo.py
"""

# ═══════════════════════════════════════════════════════════════════════════
# ЧАСТЬ 1: ОСНОВНАЯ МЕТАФОРА
# ═══════════════════════════════════════════════════════════════════════════

print("=" * 70)
print("ЧАСТЬ 1: ПЕРЕМЕННАЯ — ЭТО ИМЕНО, НЕ КОНТЕЙНЕР")
print("=" * 70)

print("""
🏠 Метафора:
  Объект в Python = дом в городе
  Переменная = почтовый ящик на этом доме

  x = [1, 2, 3]  означает:
    → Создан дом (список [1, 2, 3])
    → На этом доме повешен ящик с именем 'x'

  Ящик НЕ содержит сам список, а указывает на его адрес!
""")

x = [1, 2, 3]
print(f"x = [1, 2, 3]")
print(f"Объект находится по адресу: {id(x)}")
print(f"Переменная x — это просто имя для этого адреса\n")

# ═══════════════════════════════════════════════════════════════════════════
# ЧАСТЬ 2: ФУНКЦИЯ id() — АДРЕС В ПАМЯТИ
# ═══════════════════════════════════════════════════════════════════════════

print("=" * 70)
print("ЧАСТЬ 2: id() — АДРЕС ОБЪЕКТА В ПАМЯТИ")
print("=" * 70)

a = 42
b = "hello"
c = [1, 2, 3]

print(f"a = 42")
print(f"  id(a) = {id(a)}")
print(f"  Число 42 живет по адресу {id(a)}\n")

print(f"b = 'hello'")
print(f"  id(b) = {id(b)}")
print(f"  Строка 'hello' живет по адресу {id(b)}\n")

print(f"c = [1, 2, 3]")
print(f"  id(c) = {id(c)}")
print(f"  Список живет по адресу {id(c)}\n")

print("Каждый объект имеет уникальный адрес (id)\n")

# ═══════════════════════════════════════════════════════════════════════════
# ЧАСТЬ 3: ПРИСВАИВАНИЕ — СОЗДАНИЕ ВТОРОГО ИМЕНИ
# ═══════════════════════════════════════════════════════════════════════════

print("=" * 70)
print("ЧАСТЬ 3: x = y — ВТОРОЕ ИМЯ, НЕ КОПИРОВАНИЕ")
print("=" * 70)

x = [1, 2, 3]
y = x  # НЕ создаёт новый список, а дает второе имя

print(f"x = [1, 2, 3]")
print(f"y = x")
print(f"\nВизуально:")
print(f"  x ─┐")
print(f"     └─→ [1, 2, 3] (адрес {id(x)})")
print(f"  y ─┘\n")

print(f"id(x) = {id(x)}")
print(f"id(y) = {id(y)}")
print(f"Адреса ОДИНАКОВЫЕ → это один объект, два имени!\n")

# Изменение через x видно через y
x.append(4)
print(f"x.append(4)")
print(f"x = {x}")
print(f"y = {y}  ← y тоже изменилась! (один объект)")
print(f"Это может быть неожиданно, если думать, что y — копия!\n")

# ═══════════════════════════════════════════════════════════════════════════
# ЧАСТЬ 4: НОВОЕ ПРИСВАИВАНИЕ vs ИЗМЕНЕНИЕ ОБЪЕКТА
# ═══════════════════════════════════════════════════════════════════════════

print("=" * 70)
print("ЧАСТЬ 4: ПРИСВАИВАНИЕ vs ИЗМЕНЕНИЕ ОБЪЕКТА")
print("=" * 70)

print("""
ВАЖНОЕ РАЗЛИЧИЕ:
  x = y        → переменная x теперь указывает на объект y
  x.append()   → объект изменяется, но переменная указывает на то же место
""")

x = [1, 2, 3]
y = x

print(f"x = [1, 2, 3]")
print(f"y = x")
print(f"id(x) = {id(x)}, id(y) = {id(y)}\n")

# Изменяем объект
x.append(4)
print(f"x.append(4)")
print(f"x = {x}")
print(f"y = {y}")
print(f"id(x) = {id(x)}, id(y) = {id(y)}  ← адреса НЕ изменились\n")

# Новое присваивание
x = [5, 6, 7]
print(f"x = [5, 6, 7]  (новое присваивание)")
print(f"x = {x}")
print(f"y = {y}  ← y не изменилась! (они больше не связаны)")
print(f"id(x) = {id(x)}, id(y) = {id(y)}  ← адреса РАЗНЫЕ\n")

print("Вывод: присваивание x = [5, 6, 7] переводит переменную на новый объект,")
print("       но y остается указывать на старый объект\n")

# ═══════════════════════════════════════════════════════════════════════════
# ЧАСТЬ 5: == vs is — СМЫСЛ И ПРИМЕНЕНИЕ
# ═══════════════════════════════════════════════════════════════════════════

print("=" * 70)
print("ЧАСТЬ 5: == (ЗНАЧЕНИЕ) vs is (ИДЕНТИЧНОСТЬ)")
print("=" * 70)

# Сценарий 1: Одинаковые значения, разные объекты
a = [1, 2, 3]
b = [1, 2, 3]

print("Сценарий 1: a = [1, 2, 3], b = [1, 2, 3]")
print(f"  a == b → {a == b}  (ЗНАЧЕНИЯ одинаковые)")
print(f"  a is b → {a is b}  (РАЗНЫЕ объекты в памяти)")
print(f"  id(a) = {id(a)}")
print(f"  id(b) = {id(b)}\n")

# Сценарий 2: Один объект, два имена
a = [1, 2, 3]
b = a

print("Сценарий 2: a = [1, 2, 3], b = a")
print(f"  a == b → {a == b}  (ЗНАЧЕНИЯ одинаковые)")
print(f"  a is b → {a is b}  (ОДИН объект)")
print(f"  id(a) = {id(a)}")
print(f"  id(b) = {id(b)}\n")

# Таблица
print("┌─────────────────┬───────────────────┬────────────────────┐")
print("│ Ситуация        │ ==                │ is                 │")
print("├─────────────────┼───────────────────┼────────────────────┤")
print("│ Разные объекты  │ True (если значения│ False              │")
print("│ одинак. значен. │ одинаковые)       │                    │")
print("├─────────────────┼───────────────────┼────────────────────┤")
print("│ Один объект,    │ True              │ True               │")
print("│ два имена       │                   │                    │")
print("└─────────────────┴───────────────────┴────────────────────┘\n")

# ═══════════════════════════════════════════════════════════════════════════
# ЧАСТЬ 6: КОГДА ИСПОЛЬЗОВАТЬ == и is
# ═══════════════════════════════════════════════════════════════════════════

print("=" * 70)
print("ЧАСТЬ 6: КОГДА ИСПОЛЬЗОВАТЬ == и is")
print("=" * 70)

# Правильное использование ==
x = 42
y = 42
if x == y:
    print("✅ Правильно: x == y (сравниваем значения)\n")

# Правильное использование is None
result = None
if result is None:
    print("✅ Правильно: result is None (check for None)\n")

# Неправильное использование is для сравнения значений
a = [1, 2, 3]
b = [1, 2, 3]
if a is b:
    print("❌ Неправильно: a is b (это разные объекты!)")
else:
    print("✅ Правильно: не использовали 'is' для сравнения значений\n")

print("ПРАВИЛО:")
print("  • Используй == для сравнения ЗНАЧЕНИЙ")
print("  • Используй is для сравнения с None, True, False")
print("  • Используй is РЕДКО, только когда нужна идентичность\n")

# ═══════════════════════════════════════════════════════════════════════════
# ЧАСТЬ 7: ИНТЕРНИРОВАНИЕ — ОПТИМИЗАЦИЯ ПАМЯТИ
# ═══════════════════════════════════════════════════════════════════════════

print("=" * 70)
print("ЧАСТЬ 7: ИНТЕРНИРОВАНИЕ — ОПТИМИЗАЦИЯ ПАМЯТИ")
print("=" * 70)

print("""
Python экономит память, переиспользуя маленькие объекты.
Это называется интернированием.
""")

# Малые числа (-5 до 256)
print("МАЛЫЕ ЧИСЛА (от -5 до 256):")
x = 5
y = 5
print(f"  x = 5")
print(f"  y = 5")
print(f"  x is y → {x is y}  ✅")
print(f"  id(x) = {id(x)}")
print(f"  id(y) = {id(y)}")
print(f"  Python переиспользует одно число!\n")

# Большие числа (> 256)
print("БОЛЬШИЕ ЧИСЛА (> 256):")
a = 300
b = 300
print(f"  a = 300")
print(f"  b = 300")
print(f"  a is b → {a is b}  ❌ (обычно False, не гарантировано)")
print(f"  id(a) = {id(a)}")
print(f"  id(b) = {id(b)}")
print(f"  Python создает два разных объекта\n")

# Строки (интернируются, но непредсказуемо)
print("СТРОКИ (интернируются избирательно):")
s1 = "hello"
s2 = "hello"
print(f"  s1 = 'hello'")
print(f"  s2 = 'hello'")
print(f"  s1 is s2 → {s1 is s2}  (может быть True или False)\n")

print("ВАЖНО: Интернирование — деталь оптимизации Python,")
print("       не полагайся на его поведение в коде!\n")

# ═══════════════════════════════════════════════════════════════════════════
# ЧАСТЬ 8: ИЗМЕНЯЕМЫЕ vs НЕИЗМЕНЯЕМЫЕ ОБЪЕКТЫ
# ═══════════════════════════════════════════════════════════════════════════

print("=" * 70)
print("ЧАСТЬ 8: ИЗМЕНЯЕМЫЕ vs НЕИЗМЕНЯЕМЫЕ ОБЪЕКТЫ")
print("=" * 70)

print("""
ИЗМЕНЯЕМЫЕ (mutable): list, dict, set
  → Можно менять содержимое, но id остается тем же

НЕИЗМЕНЯЕМЫЕ (immutable): int, float, str, tuple
  → Нельзя менять содержимое, любое изменение = новый объект
""")

# Изменяемый список
print("СПИСОК (изменяемый):")
lst = [1, 2, 3]
print(f"  lst = [1, 2, 3]")
print(f"  id до: {id(lst)}")
lst.append(4)
print(f"  lst.append(4) → lst = {lst}")
print(f"  id после: {id(lst)}  ← ID НЕ ИЗМЕНИЛСЯ\n")

# Неизменяемая строка
print("СТРОКА (неизменяемая):")
s = "hello"
print(f"  s = 'hello'")
print(f"  id до: {id(s)}")
s = s + " world"
print(f"  s = s + ' world' → s = {s}")
print(f"  id после: {id(s)}  ← ID ИЗМЕНИЛСЯ (новый объект!)\n")

# Неизменяемый кортеж
print("КОРТЕЖ (неизменяемый):")
tpl = (1, 2, 3)
print(f"  tpl = (1, 2, 3)")
print(f"  id до: {id(tpl)}")
try:
    tpl[0] = 99  # Попытка изменить
except TypeError as e:
    print(f"  Попытка tpl[0] = 99 → ошибка: {e}")
print(f"  id после: {id(tpl)}  ← ID НЕ ИЗМЕНИЛСЯ (объект не менялся)\n")

print("ВЫВОД: Присваивание s = s + 'x' ВСЕГДА создает новый объект,")
print("       но lst.append(x) меняет объект на месте\n")

# ═══════════════════════════════════════════════════════════════════════════
# ИТОГОВАЯ ШПАРГАЛКА
# ═══════════════════════════════════════════════════════════════════════════

print("=" * 70)
print("✅ ИТОГОВАЯ ШПАРГАЛКА")
print("=" * 70)

print("""
1️⃣ ПЕРЕМЕННАЯ = ИМЕНО ДЛЯ ОБЪЕКТА
   x = [1, 2, 3]  ← x указывает на объект [1, 2, 3]

2️⃣ id() = АДРЕС В ПАМЯТИ
   id(x)  ← номер дома для объекта

3️⃣ ПРИСВАИВАНИЕ СОЗДАЕТ ВТОРОЕ ИМЯ
   y = x  ← y теперь указывает на тот же объект
   id(x) == id(y)  ← одинаковые адреса

4️⃣ == vs is
   ==  сравнивает ЗНАЧЕНИЯ (содержимое)
   is  сравнивает ИДЕНТИЧНОСТЬ (один объект?)

5️⃣ ИЗМЕНЯЕМОСТЬ
   list.append() меняет объект на месте (id не меняется)
   s = s + 'x'  создает новый объект (id меняется)

6️⃣ ИНТЕРНИРОВАНИЕ
   Числа -5..256: кэшируются
   Числа > 256:   не кэшируются
   Не полагайся на это в коде!

7️⃣ ПРАВИЛО: Используй ==, избегай is (кроме None/True/False)
""")
