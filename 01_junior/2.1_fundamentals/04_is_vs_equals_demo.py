"""
🧠 ДОЛГОВРЕМЕННАЯ ПАМЯТЬ: is vs == vs id()

Структурировано так, чтобы легко запомнить и повторить.
"""

# ═══════════════════════════════════════════════════════════════════════════
# 1️⃣ ОСНОВЫ: Объект и его адрес в памяти
# ═══════════════════════════════════════════════════════════════════════════

print("\n📍 ШАГ 1: Каждый объект имеет адрес в памяти\n")

a = [1, 2, 3]
print(f"Список a занимает место в памяти по адресу: {id(a)}")
print(f"Это как номер дома: у каждого объекта свой номер")

# ПОМНИ: id() — это номер дома для объекта в памяти


# ═══════════════════════════════════════════════════════════════════════════
# 2️⃣ КЛЮЧЕВАЯ РАЗНИЦА: == vs is
# ═══════════════════════════════════════════════════════════════════════════

print("\n" + "="*70)
print("⚡ ШАГ 2: == vs is — в чём разница?")
print("="*70 + "\n")

a = [1, 2, 3]
b = [1, 2, 3]

print("Два списка с одинаковым содержимым:")
print(f"  a = {a}")
print(f"  b = {b}")
print()

print("🔴 == Сравнивает СОДЕРЖИМОЕ (значения внутри)")
print(f"   a == b → {a == b}  ✅ (одинаковые значения)")
print()

print("🔵 is Сравнивает ИДЕНТИЧНОСТЬ (это один объект?)")
print(f"   a is b → {a is b}  ❌ (разные объекты в памяти)")
print()

print(f"Доказательство через id():")
print(f"   id(a) = {id(a)}")
print(f"   id(b) = {id(b)}")
print(f"   Адреса РАЗНЫЕ → это разные объекты!")

# ПОМНИ: == для значений, is для идентичности


# ═══════════════════════════════════════════════════════════════════════════
# 3️⃣ ВТОРОЕ ИМЯ: Присваивание ссылки
# ═══════════════════════════════════════════════════════════════════════════

print("\n" + "="*70)
print("⚡ ШАГ 3: c = a (второе имя для одного объекта)")
print("="*70 + "\n")

a = [1, 2, 3]
c = a  # НЕ новый список, а ВТОРОЕ ИМЯ для того же объекта

print(f"a = {a}")
print(f"c = a  ← c НЕ копирует, а просто ещё одно имя!")
print()

print(f"a == c → {a == c}  ✅ (одинаковые значения)")
print(f"a is c → {a is c}  ✅ (ОДИН объект!)")
print()

print(f"id(a) = {id(a)}")
print(f"id(c) = {id(c)}")
print(f"Адреса ОДИНАКОВЫЕ → один объект, два имена!")

# ПОМНИ: x = y создаёт второе имя, не копирует


# ═══════════════════════════════════════════════════════════════════════════
# 4️⃣ ВИЗУАЛЬНАЯ ДИАГРАММА: Память
# ═══════════════════════════════════════════════════════════════════════════

print("\n" + "="*70)
print("📊 ШАГ 4: Как это выглядит в памяти")
print("="*70 + "\n")

print("Сценарий:")
print("  a = [1, 2, 3]")
print("  b = [1, 2, 3]")
print("  c = a")
print()

print("В ПАМЯТИ:")
print("""
  ОБЪЕКТ 1: [1, 2, 3] ← адрес id(a), id(c)
    ↑      ↑
    a      c  (два имена для одного объекта)

  ОБЪЕКТ 2: [1, 2, 3] ← адрес id(b)
    ↑
    b  (другой объект)
""")

print("ИТОГ:")
print("  a == b → True  (содержимое одинаковое)")
print("  a is b → False (разные объекты)")
print("  a is c → True  (один объект)")


# ═══════════════════════════════════════════════════════════════════════════
# 5️⃣ ИНТЕРНИРОВАНИЕ: Кэширование малых чисел
# ═══════════════════════════════════════════════════════════════════════════

print("\n" + "="*70)
print("⚡ ШАГ 5: Интернирование — Python экономит память")
print("="*70 + "\n")

print("МАЛЫЕ ЧИСЛА (от -5 до 256) кэшируются:")
x = 5
y = 5
print(f"\n  x = 5")
print(f"  y = 5")
print(f"  x is y → {x is y}  ✅")
print(f"  id(x) = {id(x)}")
print(f"  id(y) = {id(y)}")
print(f"  Python переиспользует одно число 5 в памяти!")

print("\nБОЛЬШИЕ ЧИСЛА (> 256) НЕ кэшируются:")
a = 300
b = 300
print(f"\n  a = 300")
print(f"  b = 300")
print(f"  a is b → {a is b}  ❌ (обычно)")
print(f"  id(a) = {id(a)}")
print(f"  id(b) = {id(b)}")
print(f"  Python создаёт два разных объекта 300!")

# ПОМНИ: Интернирование оптимизирует память, но не влияет на логику


# ═══════════════════════════════════════════════════════════════════════════
# ✅ ИТОГОВАЯ ШПАРГАЛКА
# ═══════════════════════════════════════════════════════════════════════════

print("\n" + "="*70)
print("✅ ШПАРГАЛКА ДЛЯ БЫСТРОГО ПОВТОРА")
print("="*70 + "\n")

print("""
┌─────────────────────────────────────────────────────────────────┐
│ ПРАВИЛО 1: == vs is                                             │
├─────────────────────────────────────────────────────────────────┤
│ ==  → сравнивает ЗНАЧЕНИЯ   (содержимое одинаковое?)           │
│ is  → сравнивает ИДЕНТИЧНОСТЬ (один и тот же объект?)          │
│ id()→ возвращает АДРЕС объекта в памяти                        │
└─────────────────────────────────────────────────────────────────┘

┌─────────────────────────────────────────────────────────────────┐
│ ПРАВИЛО 2: Присваивание                                         │
├─────────────────────────────────────────────────────────────────┤
│ b = [1, 2]  → новый объект (разные id)                         │
│ c = a       → ВТОРОЕ имя (одинаковые id)                       │
└─────────────────────────────────────────────────────────────────┘

┌─────────────────────────────────────────────────────────────────┐
│ ПРАВИЛО 3: Интернирование                                       │
├─────────────────────────────────────────────────────────────────┤
│ Числа -5 до 256: кэшируются → x is y = True                    │
│ Числа > 256:     не кэшируются → a is b = False                │
│ Списки/словари:  НИКОГДА не кэшируются                         │
└─────────────────────────────────────────────────────────────────┘
""")

print("Запомни эти 3 правила — всё остальное будет легко!\n")
