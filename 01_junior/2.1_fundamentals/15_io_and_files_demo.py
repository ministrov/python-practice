# ============================================================
# ЧАСТЬ 1: print() — продвинутые параметры
# ============================================================

print("=" * 55)
print("ЧАСТЬ 1: print() с параметрами")
print("=" * 55)

# Параметр end= — что ставить в конце (по умолчанию \n)
print("Строка 1", end=" ")
print("Строка 2", end=" ")
print("Строка 3")
# → Строка 1 Строка 2 Строка 3  (всё в одну строку)

# Параметр sep= — разделитель между аргументами (по умолчанию " ")
print("яблоко", "банан", "груша", sep=", ")
# → яблоко, банан, груша

print("2026", "06", "10", sep="-")
# → 2026-06-10

# Комбинируем
print("один", "два", "три", sep=" | ", end=".\n")
# → один | два | три.


# ============================================================
# ЧАСТЬ 2: input()
# ============================================================

print("\n" + "=" * 55)
print("ЧАСТЬ 2: input()")
print("=" * 55)

# input() ВСЕГДА возвращает str — даже если ввели число
# name = input("Как тебя зовут? ")   ← читает строку
# age = int(input("Сколько лет? "))   ← нужно явно привести к int

# Демо без реального ввода (чтобы файл запускался автоматически):
simulated_input: str = "42"
age: int = int(simulated_input)
print(f"Введено: '{simulated_input}', после int(): {age}, тип: {type(age)}")

# Ловушка: забыть привести тип
num_str: str = "10"
# print(num_str + 5)   ← ошибка! нельзя сложить str и int
print(int(num_str) + 5)  # → 15


# ============================================================
# ЧАСТЬ 3: Запись файла через with
# ============================================================

print("\n" + "=" * 55)
print("ЧАСТЬ 3: Запись файла (режим 'w')")
print("=" * 55)

# with open(...) as f: — контекст-менеджер
# Файл автоматически закрывается после блока with
# Режим "w" — запись (создаёт файл или перезаписывает)
# encoding="utf-8" — всегда указывай, особенно для кириллицы

with open("demo_output.txt", "w", encoding="utf-8") as f:
    f.write("Первая строка\n")
    f.write("Вторая строка\n")
    f.write("Третья строка\n")

print("Файл demo_output.txt создан ✓")


# ============================================================
# ЧАСТЬ 4: Чтение файла через with
# ============================================================

print("\n" + "=" * 55)
print("ЧАСТЬ 4: Чтение файла (режим 'r')")
print("=" * 55)

# read() — читает весь файл как одну строку
with open("demo_output.txt", "r", encoding="utf-8") as f:
    content: str = f.read()

print("=== read() — весь файл ===")
print(content)

# readlines() — читает файл как список строк
with open("demo_output.txt", "r", encoding="utf-8") as f:
    lines: list[str] = f.readlines()

print("=== readlines() — список строк ===")
print(lines)
# → ['Первая строка\n', 'Вторая строка\n', 'Третья строка\n']

# Перебор строк напрямую — самый питоничный способ
print("=== Перебор строк ===")
with open("demo_output.txt", "r", encoding="utf-8") as f:
    for line in f:
        print(line.strip())  # strip убирает \n в конце каждой строки


# ============================================================
# ЧАСТЬ 5: Дозапись через режим "a"
# ============================================================

print("\n" + "=" * 55)
print("ЧАСТЬ 5: Дозапись (режим 'a')")
print("=" * 55)

# "a" (append) — добавляет в конец, не перезаписывает
with open("demo_output.txt", "a", encoding="utf-8") as f:
    f.write("Четвёртая строка (добавлена)\n")

print("Строка добавлена. Читаем снова:")
with open("demo_output.txt", "r", encoding="utf-8") as f:
    for line in f:
        print(line.strip())


# ============================================================
# ЧАСТЬ 6: Режимы открытия файла — сводка
# ============================================================

print("\n" + "=" * 55)
print("ЧАСТЬ 6: Режимы open()")
print("=" * 55)

modes: dict[str, str] = {
    "r":  "чтение         (файл должен существовать)",
    "w":  "запись         (создаёт или перезаписывает!)",
    "a":  "дозапись       (добавляет в конец)",
    "r+": "чтение+запись  (файл должен существовать)",
    "x":  "создание       (ошибка если файл уже есть)",
}

for mode, description in modes.items():
    print(f"  '{mode}'  →  {description}")


# ============================================================
# ЧАСТЬ 7: Запись нескольких строк через writelines()
# ============================================================

print("\n" + "=" * 55)
print("ЧАСТЬ 7: writelines()")
print("=" * 55)

# writelines() принимает список строк
# Важно: НЕ добавляет \n автоматически — ставь сам!
shopping: list[str] = ["молоко\n", "хлеб\n", "яйца\n"]

with open("shopping.txt", "w", encoding="utf-8") as f:
    f.writelines(shopping)

print("shopping.txt создан. Содержимое:")
with open("shopping.txt", "r", encoding="utf-8") as f:
    print(f.read())


