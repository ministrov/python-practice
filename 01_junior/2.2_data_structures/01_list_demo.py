nums.extend([4, 5])
print(f"РџРѕСЃР»Рµ extend([4, 5]): {nums}")  # [1, 2, 3, 4, 5]

# len() вЂ” РґР»РёРЅР° СЃРїРёСЃРєР°
print(f"len([1, 2, 3]) = {len([1, 2, 3])}")  # 3

# count() вЂ” РїРѕРґСЃС‡РёС‚Р°С‚СЊ РІС…РѕР¶РґРµРЅРёСЏ СЌР»РµРјРµРЅС‚Р°
duplicates = [1, 2, 2, 3, 2, 4]
print(f"duplicates.count(2) = {duplicates.count(2)}")  # 3

# index() вЂ” РЅР°Р№С‚Рё РёРЅРґРµРєСЃ РїРµСЂРІРѕРіРѕ РІС…РѕР¶РґРµРЅРёСЏ
print(f"duplicates.index(2) = {duplicates.index(2)}")  # 1

# clear() вЂ” СѓРґР°Р»РёС‚СЊ РІСЃРµ СЌР»РµРјРµРЅС‚С‹
