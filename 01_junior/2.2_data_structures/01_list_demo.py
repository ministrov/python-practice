# sorted() вЂ” С„СѓРЅРєС†РёСЏ, РІРѕР·РІСЂР°С‰Р°РµС‚ РЅРѕРІС‹Р№ РѕС‚СЃРѕСЂС‚РёСЂРѕРІР°РЅРЅС‹Р№ СЃРїРёСЃРѕРє
