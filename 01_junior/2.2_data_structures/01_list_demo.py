print(f"РСЃС…РѕРґРЅС‹Р№ РЅРµ РёР·РјРµРЅРёР»СЃСЏ: {original}")  # [3, 1, 4, 1, 5]

print()

# ===== РџР РћР’Р•Р РљРђ РџР РРќРђР”Р›Р•Р–РќРћРЎРўР =====
print("=== РџР РћР’Р•Р РљРђ РџР РРќРђР”Р›Р•Р–РќРћРЎРўР (in) ===\n")

