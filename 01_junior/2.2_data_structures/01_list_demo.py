print(f"РџРѕСЃР»Рµ clear(): {temp}")  # []

print()

# ===== РЎРћР РўРР РћР’РљРђ =====
print("=== РЎРћР РўРР РћР’РљРђ ===\n")

# sort() вЂ” СЃРѕСЂС‚РёСЂСѓРµС‚ СЃРїРёСЃРѕРє РЅР° РјРµСЃС‚Рµ
numbers = [3, 1, 4, 1, 5, 9, 2, 6]
numbers.sort()
print(f"РџРѕСЃР»Рµ sort(): {numbers}")  # [1, 1, 2, 3, 4, 5, 6, 9]

# sort(reverse=True) вЂ” СЃРѕСЂС‚РёСЂРѕРІРєР° РІ РѕР±СЂР°С‚РЅРѕРј РїРѕСЂСЏРґРєРµ
