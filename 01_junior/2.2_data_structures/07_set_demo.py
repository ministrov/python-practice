# 2. Р‘С‹СЃС‚СЂР°СЏ РїСЂРѕРІРµСЂРєР° РЅР°Р»РёС‡РёСЏ (РІ set Р±С‹СЃС‚СЂРµРµ, С‡РµРј РІ list)
