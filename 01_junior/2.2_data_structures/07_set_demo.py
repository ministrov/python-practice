print(f"big.issubset(small) = {big.issubset(small)}")  # False

# РќР°РґРјРЅРѕР¶РµСЃС‚РІРѕ (issuperset)
print(f"big.issuperset(small) = {big.issuperset(small)}")  # True

# РџСЂРѕРІРµСЂРєР° РЅРµСЃРѕРІРјРµСЃС‚РёРјРѕСЃС‚Рё (isdisjoint)
