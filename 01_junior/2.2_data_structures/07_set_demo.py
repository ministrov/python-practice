# ===== FROZENSET (РќР•РР—РњР•РќРЇР•РњРћР• РњРќРћР–Р•РЎРўР’Рћ) =====
print("=== FROZENSET (РЅРµРёР·РјРµРЅСЏРµРјРѕРµ РјРЅРѕР¶РµСЃС‚РІРѕ) ===\n")

