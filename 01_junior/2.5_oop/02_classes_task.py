class Person:
    def __init__(self, name: str, age: int):
        self.name = name
        self.age = age

    def introduce(self) -> str:
        return f"Меня зовут {self.name}, мне {self.age} лет"


victor = Person("Victor", 34)
maria = Person("Maria", 23)

print(victor.name)
print(victor.age)

print(maria.name)
print(maria.age)

