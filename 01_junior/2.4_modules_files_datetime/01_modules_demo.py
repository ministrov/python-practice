# -*- coding: utf-8 -*-
"""
Блок 2.4.1: Демо — модули
════════════════════════════════════════════════════════════════════════
Темы:
  1. import модуля целиком, обращение через точку
  2. from модуль import имя — импорт конкретных имён
  3. import ... as ... — псевдоним
  4. Что происходит при импорте: модуль выполняется один раз, кэшируется
  5. __name__: "__main__" при запуске, имя модуля — при импорте
  6. if __name__ == "__main__" — код только для прямого запуска
  7. Пакеты: __init__.py, импорт из пакета
  8. Поиск модулей: sys.path (обзорно)

Рядом лежат вспомогательные файлы (не редактировать, используются):
  geometry.py                          — обычный модуль
  shapes_pkg/__init__.py + circle.py   — маленький пакет
"""

# ════════════════════════════════════════════════════════════════════════
# 1. import модуля целиком
# ════════════════════════════════════════════════════════════════════════
# import ищет файл geometry.py, ВЫПОЛНЯЕТ его от начала до конца один раз
# и кладёт результат (объект модуля) в переменную geometry. Всё, что
# определено в geometry.py на верхнем уровне (функции, константы, print),
# становится доступно через geometry.<имя>.

import geometry  # эта строка печатает "[geometry] модуль загружается..."
                  # — это выполнился print() из geometry.py, а не отсюда

print(geometry.PI)               # 3.14159
print(geometry.circle_area(2))   # 12.56636


# ════════════════════════════════════════════════════════════════════════
# 2. from модуль import имя — импорт конкретных имён
# ════════════════════════════════════════════════════════════════════════
# Забирает конкретные имена прямо в текущее пространство имён — без
# префикса geometry. Модуль geometry уже загружен (см. тему 4), поэтому
# повторного print() из geometry.py здесь НЕ будет.

from geometry import circle_circumference, rectangle_area

print(circle_circumference(2))   # 12.56636
print(rectangle_area(3, 4))      # 12


# ════════════════════════════════════════════════════════════════════════
# 3. import ... as ... — псевдоним
# ════════════════════════════════════════════════════════════════════════
# Удобно для длинных имён модулей или чтобы не занимать короткое имя в
# своём коде. Стандартные псевдонимы в индустрии: `import numpy as np`,
# `import pandas as pd`.

import geometry as geo

print(geo.circle_area(1))   # 3.14159
print(geo is geometry)      # True — это ОДИН и тот же объект модуля


# ════════════════════════════════════════════════════════════════════════
# 4. Что происходит при импорте: выполняется ОДИН раз
# ════════════════════════════════════════════════════════════════════════
# Python кэширует уже импортированные модули в sys.modules. Повторный
# import того же модуля (в этом же процессе) НЕ выполняет файл заново —
# просто возвращает уже готовый объект из кэша.

import sys

print("geometry" in sys.modules)   # True

# Намеренный повторный import (демонстрация кэширования, не ошибка):
import geometry  # pyright: ignore[reportDuplicateImport]
# print() из geometry.py НЕ появится снова — модуль уже в sys.modules


# ════════════════════════════════════════════════════════════════════════
# 5. __name__: "__main__" при запуске, имя модуля — при импорте
# ════════════════════════════════════════════════════════════════════════
# У каждого модуля есть переменная __name__. Если файл ЗАПУЩЕН напрямую
# (python geometry.py) — __name__ == "__main__". Если файл ИМПОРТИРОВАН
# из другого файла — __name__ равен имени модуля ("geometry").

print(__name__)            # "__main__" — этот файл запущен напрямую
print(geometry.__name__)   # "geometry" — geometry импортирован сюда


# ════════════════════════════════════════════════════════════════════════
# 6. if __name__ == "__main__" — код только для прямого запуска
# ════════════════════════════════════════════════════════════════════════
# Внизу geometry.py есть блок:
#     if __name__ == "__main__":
#         print("geometry.py запущен напрямую (не импортирован)")
#         ...
# Он НЕ сработал, когда мы делали import geometry выше — там __name__
# был "geometry", а не "__main__". Он сработает, только если запустить
# geometry.py напрямую: `python geometry.py`. Попробуй это в терминале
# отдельно от текущего demo-файла — увидишь дополнительный вывод.
#
# Смысл паттерна: модуль можно одновременно и импортировать (как
# библиотеку функций), и запускать напрямую (для теста/демонстрации) —
# код для второго случая не будет мешать первому.


# ════════════════════════════════════════════════════════════════════════
# 7. Пакеты: __init__.py
# ════════════════════════════════════════════════════════════════════════
# Пакет — это папка с файлом __init__.py внутри (плюс, как правило,
# другие .py модули). __init__.py выполняется при первом импорте ЛЮБОГО
# имени из пакета и может "поднимать" имена из вложенных модулей наверх,
# чтобы снаружи не писать shapes_pkg.circle.describe_circle.

from shapes_pkg import describe_circle
# печатает "[shapes_pkg] пакет инициализируется..." — это __init__.py
# выполнился при первом обращении к пакету

print(describe_circle(5))   # "Круг радиусом 5"


# ════════════════════════════════════════════════════════════════════════
# 8. Поиск модулей: sys.path (обзорно)
# ════════════════════════════════════════════════════════════════════════
# Когда пишешь import geometry, Python ищет geometry.py по списку папок
# в sys.path. Первая запись — обычно папка самого запущенного скрипта
# (поэтому import geometry сработал без настройки: geometry.py лежит
# рядом с этим demo-файлом). Дальше идут stdlib и установленные пакеты.

print(sys.path[0])   # путь к папке, где лежит этот demo-файл

# Абсолютные импорты (import geometry, from shapes_pkg import ...) ищут
# модуль от корня, который Python видит в sys.path. Относительные
# импорты (from .circle import describe_circle — с точкой, как в
# shapes_pkg/__init__.py) работают ТОЛЬКО внутри пакета и указывают путь
# относительно текущего модуля. За пределами пакетов их не используют.
