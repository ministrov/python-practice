# -*- coding: utf-8 -*-
"""
Блок 2.6.1: Демо — основы обработки ошибок
════════════════════════════════════════════════════════════════════════
Темы:
  1. Зачем нужен try/except — необработанное исключение роняет программу
  2. Базовый try/except, ловля конкретного типа
  3. Несколько except подряд — порядок от частного к общему
  4. Несколько типов в одном except, доступ к сообщению через as e
  5. else — код, который выполняется, только если исключения не было
  6. finally — код, который выполняется ВСЕГДА
  7. Иерархия исключений: except Exception ловит и потомков
  8. raise — поднять исключение вручную; re-raise без аргументов
  9. Свои классы исключений
"""


# ════════════════════════════════════════════════════════════════════════
# 1. Зачем нужен try/except
# ════════════════════════════════════════════════════════════════════════
# Без обработки необработанное исключение останавливает всю программу —
# даже если упала одна операция из тысячи. try/except даёт код, который
# выполнится ВМЕСТО падения, когда что-то пошло не так.

# 10 / 0  # если раскомментировать — уронит программу: ZeroDivisionError


# ════════════════════════════════════════════════════════════════════════
# 2. Базовый try/except
# ════════════════════════════════════════════════════════════════════════
# try: код, который может упасть.
# except ТипОшибки: код, который выполнится, если упала именно эта ошибка.

try:
    result = 10 / 0
except ZeroDivisionError:
    print("Деление на ноль!")  # выполнится это

# Если бы тип ошибки в except не совпал с реальной ошибкой — except её
# бы НЕ поймал, и программа упала бы дальше как обычно.


# ════════════════════════════════════════════════════════════════════════
# 3. Несколько except подряд — от частного к общему
# ════════════════════════════════════════════════════════════════════════
# Python проверяет except по порядку СВЕРХУ ВНИЗ и выполняет ПЕРВЫЙ
# подходящий. Значит, более конкретные типы должны идти раньше более
# общих — иначе общий "перехватит" их первым, и до частного дело не
# дойдёт (недостижимый код, некоторые линтеры это подсвечивают).

def parse_and_divide(a: str, b: str) -> float | None:
    """Парсит две строки в числа и делит первое на второе."""
    try:
        return int(a) / int(b)
    except ZeroDivisionError:
        print("Нельзя делить на ноль")
        return None
    except ValueError:
        print(f"'{a}' или '{b}' — не число")
        return None


print(parse_and_divide("10", "2"))   # 5.0
print(parse_and_divide("10", "0"))   # None, "Нельзя делить на ноль"
print(parse_and_divide("abc", "2"))  # None, "'abc' или '2' — не число"


# ════════════════════════════════════════════════════════════════════════
# 4. Несколько типов в одном except + as e
# ════════════════════════════════════════════════════════════════════════
# Если для двух разных ошибок нужна ОДНА и та же обработка — не дублируем
# except, а перечисляем типы кортежем: except (A, B) as e.
# `as e` даёт доступ к самому объекту исключения — у него есть текстовое
# сообщение через str(e) (или f"{e}").

def get_element(items: list[int], index: int | str) -> int | None:
    """Возвращает items[index] или None, если это невозможно."""
    try:
        return items[index]  # type: ignore
    except (IndexError, TypeError) as e:
        print(f"Не удалось получить элемент: {type(e).__name__}: {e}")
        return None


print(get_element([1, 2, 3], 1))     # 2
print(get_element([1, 2, 3], 99))    # None, IndexError: list index out of range
print(get_element([1, 2, 3], "1"))   # None, TypeError: ...


# ════════════════════════════════════════════════════════════════════════
# 5. else — только если исключения НЕ было
# ════════════════════════════════════════════════════════════════════════
# Код в else выполняется ТОЛЬКО если try отработал без ошибок. Отличие от
# простого продолжения строк после try/except: если положить эти строки
# просто ПОСЛЕ блока, они выполнятся даже после того, как except поймал
# ошибку. else — явный сигнал "это только для успешного случая".

def safe_sqrt(n: float) -> float | None:
    try:
        if n < 0:
            raise ValueError("нельзя извлечь корень из отрицательного числа")
        value = n ** 0.5
    except ValueError as e:
        print(f"Ошибка: {e}")
        return None
    else:
        print(f"Корень успешно вычислен: {value}")
        return value


safe_sqrt(16)   # "Корень успешно вычислен: 4.0"
safe_sqrt(-4)   # "Ошибка: нельзя извлечь корень из отрицательного числа"


# ════════════════════════════════════════════════════════════════════════
# 6. finally — выполняется ВСЕГДА
# ════════════════════════════════════════════════════════════════════════
# finally срабатывает и при успехе, и при ошибке, и даже если внутри
# try/except был return. Типичное применение — освобождение ресурсов
# (закрыть файл, соединение), которое должно случиться в любом случае.

def divide_with_log(a: float, b: float) -> float | None:
    try:
        return a / b
    except ZeroDivisionError:
        print("Ошибка деления")
        return None
    finally:
        print(f"Попытка деления {a} / {b} завершена")  # выполнится всегда


divide_with_log(10, 2)  # 5.0, затем "Попытка деления 10 / 2 завершена"
divide_with_log(10, 0)  # "Ошибка деления", затем "Попытка деления 10 / 0 завершена"


# ════════════════════════════════════════════════════════════════════════
# 7. Иерархия исключений
# ════════════════════════════════════════════════════════════════════════
# Встроенные исключения образуют дерево наследования:
#   BaseException
#     └── Exception                 <- почти всё, что стоит ловить
#           ├── ValueError
#           ├── TypeError
#           ├── LookupError
#           │     ├── IndexError
#           │     └── KeyError
#           ├── ArithmeticError
#           │     └── ZeroDivisionError
#           └── ...
# except Exception ловит ЛЮБОЕ исключение-потомок Exception — включая
# ValueError, TypeError, IndexError и т.д., даже если явно их не называли.
# Голый `except:` (без типа) ловит вообще всё, включая KeyboardInterrupt
# и SystemExit (потомки BaseException, не Exception) — поэтому голый
# except считается плохой практикой: он мешает даже Ctrl+C прервать
# программу штатно.

try:
    [1, 2, 3][10]
except Exception as e:
    # IndexError — потомок Exception, поэтому ловится здесь,
    # хотя явно указан только родительский тип
    print(f"Поймано через общий Exception: {type(e).__name__}: {e}")


# ════════════════════════════════════════════════════════════════════════
# 8. raise — поднять исключение вручную
# ════════════════════════════════════════════════════════════════════════
# raise ТипОшибки("сообщение") — явно сигнализирует о проблеме в том
# месте, где программа её обнаружила, а не там, где она "случайно"
# вылезет сама. Это идиоматичнее, чем возвращать None/-1 молча.

def set_age(age: int) -> int:
    if age < 0:
        raise ValueError(f"Возраст не может быть отрицательным: {age}")
    return age


try:
    set_age(-5)
except ValueError as e:
    print(f"Отловлено: {e}")

# raise без аргументов ВНУТРИ except — "передать ошибку дальше" (re-raise)
# после того как, например, залогировали её здесь же.

def risky_call() -> None:
    try:
        int("not a number")
    except ValueError:
        print("Залогировали ошибку, пробрасываем дальше")
        raise  # поднимает ТУ ЖЕ самую пойманную ошибку


try:
    risky_call()
except ValueError as e:
    print(f"Поймано снаружи: {e}")


# ════════════════════════════════════════════════════════════════════════
# 9. Свои классы исключений
# ════════════════════════════════════════════════════════════════════════
# Своё исключение — обычный класс, унаследованный от Exception (или от
# более конкретного встроенного класса, если это уместно по смыслу).
# Даёт вызывающему коду ловить именно ЭТУ ошибку отдельно от прочих,
# и позволяет хранить дополнительные данные об ошибке (не только текст).

class InsufficientFundsError(Exception):
    """Поднимается, когда на счёте недостаточно средств для списания."""

    def __init__(self, balance: float, amount: float) -> None:
        self.balance = balance
        self.amount = amount
        super().__init__(
            f"Недостаточно средств: баланс {balance}, "
            f"запрошено {amount}"
        )


def withdraw(balance: float, amount: float) -> float:
    if amount > balance:
        raise InsufficientFundsError(balance, amount)
    return balance - amount


try:
    withdraw(100, 150)
except InsufficientFundsError as e:
    print(f"Отказ: {e}")
    print(f"Не хватило: {e.amount - e.balance}")  # доступ к своим атрибутам
