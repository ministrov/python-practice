# -*- coding: utf-8 -*-
"""
Базовая тренировка словарей — dict drill
Одно действие за раз. Читай задание, пиши код под ним.
"""

# ─────────────────────────────────────────
# 1. СОЗДАЙ словарь человека
# ─────────────────────────────────────────
# Создай переменную person со словарём:
# имя "Ivan", возраст 25, город "Moscow"

# ТВОЙ КОД:
person = {"name": "Ivan", "age": 25, "city": "Moscow"}
print(person)
print(type(person))

# ─────────────────────────────────────────
# 2. ПОЛУЧИ значения по ключу
# ─────────────────────────────────────────
# Напечатай имя из словаря person
# Напечатай возраст

# ТВОЙ КОД:
print(person["name"])
print(person["age"])

# ─────────────────────────────────────────
# 3. БЕЗОПАСНЫЙ доступ через get()
# ─────────────────────────────────────────
# Попробуй получить ключ "email" через get()
# Если нет — выведи "нет email"

# ТВОЙ КОД:
print(person.get("email", "нет email"))

# ─────────────────────────────────────────
# 4. ИЗМЕНИ значение
# ─────────────────────────────────────────
# Измени возраст на 26
# Напечатай словарь целиком

# ТВОЙ КОД:
person["age"] = 26
print(person)

# ─────────────────────────────────────────
# 5. ДОБАВЬ новый ключ
# ─────────────────────────────────────────
# Добавь ключ "job" со значением "developer"
# Напечатай словарь

# ТВОЙ КОД:
person["job"] = "developer"
print(person)

# ─────────────────────────────────────────
# 6. УДАЛИ ключ через del
# ─────────────────────────────────────────
# Удали ключ "city"
# Напечатай словарь

# ТВОЙ КОД:
del person["city"]
print(person)

# ─────────────────────────────────────────
# 7. УДАЛИ ключ через pop() и сохрани значение
# ─────────────────────────────────────────
# Удали "job" через pop() и сохрани удалённое значение в переменную
# Напечатай: что удалили и что осталось в словаре

# ТВОЙ КОД:
job_key = person.pop("job")
print(job_key)
print(person)
# ─────────────────────────────────────────
# 8. ПРОВЕРЬ наличие ключа
# ─────────────────────────────────────────
# Проверь есть ли ключ "name" в словаре  → напечатай True/False
# Проверь есть ли ключ "city"            → напечатай True/False

# ТВОЙ КОД:
print("name" in person)
print("city" in person)

# ─────────────────────────────────────────
# 9. ПРОЙДИСЬ по ключам
# ─────────────────────────────────────────
# Напечатай каждый ключ словаря person через цикл for

# ТВОЙ КОД:
for key in person:
    print(f"{key}")

# ─────────────────────────────────────────
# 10. ПРОЙДИСЬ по значениям
# ─────────────────────────────────────────
# Напечатай каждое значение через .values()

# ТВОЙ КОД:
for value in person.values():
    print(f"{value}")

# ─────────────────────────────────────────
# 11. ПРОЙДИСЬ по парам ключ-значение
# ─────────────────────────────────────────
# Напечатай каждую пару через .items()
# Формат: "name -> Ivan"

# ТВОЙ КОД:
for key, value in person.items():
    print(f"{key} -> {value}")

# ─────────────────────────────────────────
# 12. ПОСЧИТАЙ элементы
# ─────────────────────────────────────────
# Напечатай количество ключей в словаре через len()

# ТВОЙ КОД:
print(len(person))

# ─────────────────────────────────────────
# 13. ОБНОВИ сразу несколько ключей через update()
# ─────────────────────────────────────────
# Добавь сразу два ключа: "email": "ivan@mail.ru", "phone": "89001234567"
# Напечатай финальный словарь

# ТВОЙ КОД:
person.update({"email": "ivan@mail.ru", "phone": "89001234567"})
print(person)

# ─────────────────────────────────────────
# 14. ИТОГ: напечатай красиво
# ─────────────────────────────────────────
# Пройдись по финальному словарю и напечатай каждую пару в формате:
# "Ключ: name    | Значение: Ivan"

# ТВОЙ КОД:
for key, value in person.items():
    print(f"Ключ: {key}     | Значение: {value}")
