""" Задача: Анализатор оценок студентов
    Напиши программу, которая:
        1. Просит пользователя ввести имя студента
        2. Просит ввести 5 оценок (числа от 1 до 5)
        3. Вычисляет средний балл
        4. Выводит результаты в формате:
        - Студент: [имя]
        - Оценки: [список через запятую]
        - Средний балл: [число с 2 десятичными местами]
        - Статус: в зависимости от среднего балла:
            - ≥ 4.5 → "Отличник"
            - ≥ 3.5 → "Хорошист"
            - ≥ 2.5 → "Удовлетворительно"
            - < 2.5 → "Нужна помощь"
"""

user_name = input("Введите имя студента: ")
grades: list[int] = []

for i in range(5):
    grade = int(input(f"Введите оценку {i + 1}: "))
    grades.append(grade)

# Вычисляем средний балл
average = sum(grades) / len(grades)

# Определяем статус
if average >= 4.5:
    status = "Отличник"
elif average >= 3.5:
    status = "Хорошист"
elif average >= 2.5:
    status = "Удовлетворительно"
else:
    status = "Нужна помощь"

# Форматируем оценки в строку
grades_str = ", ".join(str(g) for g in grades)

# Выводим результаты
print("\n=== РЕЗУЛЬТАТЫ ===")
print(f"Студент: {user_name}")
print(f"Оценки: {grades_str}")
print(f"Средний балл: {average:.2f}")
print(f"Статус: {status}")
